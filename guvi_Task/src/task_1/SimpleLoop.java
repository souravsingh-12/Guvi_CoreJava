package task_1;

//Write a program to print the numbers from 10 to 50 using for loop/while loop.

public class SimpleLoop {
    public static void main(String[] args) {
        
        int count =10;
        while(count >=10 && count <50){
            count++;
            System.out.println("The count is :" +count);
        }
    }
}

